


# write mlen which return the size of the list using 'for' or 'while' function

def mlen(l):
    r = 0
    while r < len(l):
        r+= 1
    return r

# Define function sum_up_to_k which sums all natural numbers up to some given number k

def sum_up_to(k):
    r = 0
    for i in range(k+1):
        r += i
    return r









